// Demonstrate TreeSet.
import java.util.*;
class TreeSetDemo {
	public static void main(String args[]) {
		// Create a tree set.
		TreeSet<String> ts = new TreeSet<String>();
		
		// Add elements to the tree set.
		ts.add("Bsad");
		ts.add("Adfd");
		ts.add("Dsds");
		ts.add("E");
		ts.add("C");
		ts.add("F");
		System.out.println(ts);
	}
}